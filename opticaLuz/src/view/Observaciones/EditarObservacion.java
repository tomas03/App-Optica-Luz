    private void editarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editarActionPerformed
        if(!"".equals(cerca.getText())||!"".equals(dni.getText())||!"".equals(lejos.getText())||!"".equals(lentelejos.getText())||!"".equals(lentecerca.getText())||!"".equals(marcolejos.getText())||!"".equals(marcocerca.getText())){
            ob.setDni(Integer.parseInt(dni.getText()));
            ob.setLejos(lejos.getText());
            ob.setLejosLente(lentelejos.getText());
            ob.setLejosMarco(marcolejos.getText());
            ob.setCerca(cerca.getText());
            ob.setCercaLente(lentecerca.getText());
            ob.setCercaMarco(marcocerca.getText());
            obdao.EdOBS(ob);
            JOptionPane.showMessageDialog(null, "Observacion Editada");
        }else{
            JOptionPane.showMessageDialog(null, "alguno o todos los campos están vacíos");
        }
        CleanTable();
        listobs();
    }//GEN-LAST:event_editarActionPerformed

