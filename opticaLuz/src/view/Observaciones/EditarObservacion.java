    public void listobs(){
        
        List<Observacion> Listob = obdao.ListOBS();
        modelo = (DefaultTableModel) tablaob.getModel();
        Object[] obj = new Object[8];
        for(int i=0;i<Listob.size();i++){
            obj[0]=Listob.get(i).getDni();
            obj[1]=Listob.get(i).getFecha();
            obj[2]=Listob.get(i).getLejos();
            obj[3]=Listob.get(i).getLejosLente();
            obj[4]=Listob.get(i).getLejosMarco();
            obj[5]=Listob.get(i).getCerca();
            obj[6]=Listob.get(i).getCercaLente();
            obj[7]=Listob.get(i).getCercaMarco();
            
            modelo.addRow(obj);
        }
        tablaob.setModel(modelo);
    }
    public void searchclient(){
        CleanTable();
        List<Observacion> listob = obdao.SearchOBS(Integer.parseInt(dnibusqueda.getText()));
        modelo = (DefaultTableModel) tablaob.getModel();
        Object[] objeto = new Object[8];
        for(int i=0;i<listob.size();i++){
            objeto[0]=listob.get(i).getDni();
            objeto[1]=listob.get(i).getFecha();
            objeto[2]=listob.get(i).getLejos();
            objeto[3]=listob.get(i).getLejosLente();
            objeto[4]=listob.get(i).getLejosMarco();
            objeto[5]=listob.get(i).getCerca();
            objeto[6]=listob.get(i).getCercaLente();
            objeto[7]=listob.get(i).getCercaMarco();
            modelo.addRow(objeto);
        }
        tablaob.setModel(modelo);
    }
    public void CleanTable(){
        for(int i =0;i<modelo.getRowCount();i++){
            modelo.removeRow(i);
            i = i-1;
        }
    }

